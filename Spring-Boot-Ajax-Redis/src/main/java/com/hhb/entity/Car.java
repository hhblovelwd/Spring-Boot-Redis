package com.hhb.entity;

public class Car {
    private int id;
    private String name;  
    private int privce;

    public int getPrivce() {
        return privce;
    }

    public void setPrivce(int privce) {
        this.privce = privce;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
